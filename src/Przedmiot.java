public class Przedmiot {

    private String nazwaP;
    double wagaP;

    public Przedmiot(String nazwaP, double wagaP) {
        this.nazwaP = nazwaP;
        this.wagaP = wagaP;
    }


    public String getNazwaP() {
        return nazwaP;
    }

    public double getWagaP() {
        return wagaP;
    }
}
